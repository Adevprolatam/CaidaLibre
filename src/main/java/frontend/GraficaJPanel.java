    private void createAndShowChart() {
        SwingUtilities.invokeLater(() -> {
            JFreeChart chart = createLineChart();
            ChartPanel chartPanel = new ChartPanel(chart);
            chartPanel.setVisible(true);
            jPanelGrafica.add(chartPanel);
            jPanelGrafica.revalidate();
            jPanelGrafica.repaint();
            System.out.println("Tama침o del panel: " + jPanelGrafica.getSize());
            System.out.println("Gr치fica creada y mostrada correctamente.");
        });
    }

    private JFreeChart createLineChart() {
        List<Caida_Libre> listaCaidaLibre = crud.obtenerListaCaidaLibre();
        CategoryDataset dataset = createDataset(listaCaidaLibre);
        JFreeChart chart = ChartFactory.createLineChart(
                "Gr치fico Lineal de Caida Libre",
                "Altura",
                "Tiempo",
                dataset
        );
        
        CategoryPlot plot = (CategoryPlot) chart.getPlot();
        plot.getRenderer().setSeriesPaint(0, new Color(70, 130, 180));
        return chart;
    }

    private CategoryDataset createDataset(List<Caida_Libre> listaCaidaLibre) {
        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        for (Caida_Libre caidaLibre : listaCaidaLibre) {    
            dataset.addValue(caidaLibre.getTiempo(), "Tiempo", String.valueOf(caidaLibre.getAltura()));
        }
        return dataset;
    }

    
